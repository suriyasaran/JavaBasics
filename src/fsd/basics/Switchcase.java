package fsd.basics;

public class Switchcase {
	
	public static void main(String[] args) {
		int i = 3;
		switch (i) {
		case 1:
		System.out.println("logic 1");
		break;
		case 2:
			System.out.println("logic 2");
			break;
		case 3:
			System.out.println("logic 3");
			break;
		case 4:
			System.out.println("logic 4");
			break;
		}
	}

}
