package task;
import java.util.*;
public class factorialWhileloop {
	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		System.out.println("Enter a number:");
		int num=s.nextInt();
		int result =1, i=1;
		while (i<=num);
		{
			result=result*i;
			i++;
	}
	System.out.println(result);
		
	}

}
